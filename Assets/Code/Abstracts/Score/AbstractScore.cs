using System.Collections;
using System.Collections.Generic;
using UnityEngine;

/**************************************************/
/**************************************************/

/**************************/
/***** ABSTRACT SCORE *****/
/**************************/

abstract public class AbstractScore : IScore
{
    /*********************/
    /***** ATTRIBUTS *****/
    /*********************/

    /**
     * @var int _value score value
     */

    protected int _value = 0;

    /**************************************************/
    /**************************************************/

    /*********************/
    /***** CONSTRUCT *****/
    /*********************/

    /**
     * @access protected
     */

    protected AbstractScore()
    {

    }

    /**************************************************/
    /**************************************************/

    /*******************************/
    /***** VALUE GETTER/SETTER *****/
    /*******************************/

    /**
     * @access public
     */

    public int Value 
    {
        get { return _value; }
        set { _value = value; }
    }

    /**************************************************/
    /**************************************************/

    /*********************************/
    /***** ISCORE INCREASE SCORE *****/
    /*********************************/

    /**
     * @access public
     */

    public virtual void IncreaseScore(int value)
    {
        _value += 1;
    }

    /**************************************************/
    /**************************************************/

    /*********************************/
    /***** ISTATE DECREASE SCORE *****/
    /*********************************/

    /**
     * @access public
     */

    public virtual void DecreaseScore(int value)
    {
        _value -= 1;
    }

    /**************************************************/
    /**************************************************/

    /******************************/
    /***** ISTATE RESET SCORE *****/
    /******************************/

    /**
     * @access public
     */

    public virtual void ResetScore()
    {
        _value = 0;
    }

}